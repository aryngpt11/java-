package Arrrayy;

public class SumOfElements {
    public static void main(String[] args) {
        int a[]={10,20,30,40,50,60};
        int summ=0;
        for(int i=0;i<a.length;i++){
            summ=summ+a[i];

        }
        System.out.println("The sum of arrays elements are: "+summ);
    }
}
